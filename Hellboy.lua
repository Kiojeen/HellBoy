function loadconfigs()
    local cfgs = io.open('/sdcard/Hellboy.kj', 'r')
    if cfgs == nil then
        saveconfigs()
        return
    end
    cfgs = cfgs:read("*a")
    if cfgs == nil then
        saveconfigs()
    elseif cfgs ~= nil then
        local bool, msg = pcall(load, cfgs)
        if bool then
            load(msg)()
            if #savedlocats == 0 then
                saveconfigs()
            end
        else
            saveconfigs()
        end
    else
        saveconfigs()
    end
end
