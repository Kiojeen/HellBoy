        local str = " {\n"
        for i, v in pairs(array) do
            if type(i) == "string" then
                str = str .. "\t\t" .. i .. " ="
            end
            if type(v) == "table" then
                str = str .. kj.tableToString(v)
            elseif type(v) == "boolean" then
                str = str .. " " .. tostring(v)
            elseif type(v) == 'string' then
                str = str .. " " .. "'" .. tostring(v) .. "'"
            else
                str =  str .. " " .. v
            end
            str = str .. ",\n"
        end
        if str ~= '' then
            str = string.sub(str, 1, string.len(str) - 1)
        end 
        str = str .. "\n}"
        return str
    end,
