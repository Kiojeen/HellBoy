function offseter()
    bootloader = gg.getRangesList("libBootloader.so")[1].start
    anptr = kj.getValue(bootloader + liboffsets.libanptr, 'Q')
    guiptr = kj.getValue(bootloader + liboffsets.libguiptr, 'Q')
    ls = {
        {
            address = anptr,
            flags = gg.TYPE_QWORD,
            name = 'anptr',
        },
        {
            address = guiptr,
            flags = gg.TYPE_QWORD,
            name = 'guiptr',
        }
    }
    gg.addListItems(ls)
end
function isfrozen(add)
    local temp = gg.getListItems()
    for i, v in ipairs(temp) do
        if v.address == add then
            if v.freeze == true then
                return true
            elseif v.freeze == false then
                return false
            end
        end
    end
end
function saveconfigs()
    local cfgs = io.open('/sdcard/Hellboy.kj', 'w')
    cfgs:write('configs = '.. kj.tableToString(configs))
    cfgs:close()
end
function loadconfigs()
    local cfgs = io.open('/sdcard/Hellboy.kj', 'r')
    if cfgs == nil then
        saveconfigs()
        return
    end
    cfgs = cfgs:read("*a")
    if cfgs == nil then
        saveconfigs()
    elseif cfgs ~= nil then
        local bool, msg = pcall(load, cfgs)
        if bool then
            load(msg)()
        else
            saveconfigs()
        end
    else
        saveconfigs()
    end
end
function version_check()
    package = gg.getTargetPackage()
    version = gg.getTargetInfo().versionCode
    if package == sky.live.package then
        hellboy = "-[" .. debug.getinfo(launch).lastlinedefined + 1 .. "]-"
        gg.toast('[Live]HellBoy\t' .. hellboy)
        if version == sky.live.version then
            anptroffsets = lanptroffsets
            gptoffsets = lgptoffsets
            distances = ldistances
            liboffsets = lliboffsets
        elseif version > sky.live.version then
            gg.alert("Sky version mismatch\nUpdate your game")
            os.exit()
        elseif version < sky.live.version then
            gg.alert("Sky version mismatch\nWait for the script to be updated")
            os.exit()
        end
    elseif package == sky.beta.package then
        hellboy = "-[" .. debug.getinfo(launch).lastlinedefined + 1 .. "]-"
        gg.toast('[Beta]HellBoy\t' .. hellboy)
        if version == sky.beta.version then
            anptroffsets = banptroffsets
            gptoffsets = bgptoffsets
            distances = bdistances
            liboffsets = bliboffsets
        elseif version > sky.beta.version then
            gg.alert("Sky version mismatch\nUpdate your game")
            os.exit()
        elseif version < sky.beta.version then
            gg.alert("Sky version mismatch\nWait for the script to be updated")
            os.exit()
        end
    else
        gg.alert("Sky package mismatch\nSelect the right process")
        os.exit()
    end
end
