                    original_value = tonumber(string.sub(original_value, 1, string.len(original_value) - 1))
                    if new_value == current_value then
                        var = {
                            {
                                address = add,
                                flags = flag_1,
                                value = original_value,
                            }
                        }
                        if toast ~= nil then
                            toast = toast .. ": " .. "OFF"
                            gg.toast(toast)
                        end 
                        gg.setValues(var)
                        return ""
                    elseif original_value == current_value then
                        var = {
                            {
                                address = add,
                                flags = flag_2,
                                value = new_value,
                            }
                        }
                        if toast ~= nil then
                            toast = toast .. ": " .. "ON"
                            gg.toast(toast)
                        end 
                        gg.setValues(var)
                        return "  -〘 ✅ 〙"
                    else
                        gg.toast("No Match")
                        return ""
                    end
                end
            end
        end
    end,
    freezeSwitch = function(add, new_value, toast)
        if type(new_value) == 'string' then
            flag, bool = kj.dT(string.sub(new_value, string.len(new_value)))
            if bool then
                new_value = tonumber(string.sub(new_value, 1, string.len(new_value) - 1))
                if type(add) == 'number' then
                    local var = {
                        {
                            address = add,
                            flags = flag,
                            value = new_value,
                            freeze = true
                        }
                    }
                    if kj.isFrozen(add) then
                        gg.removeListItems(var)
                        if toast ~= nil then
                            toast = toast .. ": " .. "OFF"
                            gg.toast(toast)
                        end
                        return ""
                    else
                        gg.setValues(var)
                        gg.addListItems(var)
                        if toast ~= nil then
                            toast = toast .. ": " .. "ON"
                            gg.toast(toast)
                        end
                        return "  -〘 ✅ 〙"
                    end
                end
            end
        end
    end,
    groupFreezeSwitch = function(array, toast, interval)
        if kj.isFrozen(array[1].address) then
            gg.removeListItems(array)
            if toast ~= nil then
                toast = toast .. ": " .. "OFF"
                gg.toast(toast)
            end
            return ""
        else
            for i, v in ipairs(array) do
                v.freeze = true
            end
            if interval == nil then
                gg.setValues(array)
                gg.addListItems(array)
                if toast ~= nil then
                    toast = toast .. ": " .. "ON"
                    gg.toast(toast)
                end
            else
                for i, v in ipairs(array) do
                    gg.setValues(v)
                    gg.addListItems(v)
                    gg.sleep(interval)
                end
                if toast ~= nil then
                    toast = toast .. ": " .. "ON"
                    gg.toast(toast)
                end
            end
            return "  -〘 ✅ 〙"
        end
    end,
}
