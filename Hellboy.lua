                    gg.toast(toast)
                end
            else
                for i, v in ipairs(array) do
                    gg.setValues(v)
                    gg.addListItems(v)
                    gg.sleep(interval)
                end
                if toast ~= nil then
                    toast = toast .. ": " .. "ON"
