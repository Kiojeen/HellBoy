                            gg.toast(toast)
                        end 
                        gg.setValues(var)
                        return "  -〘 ✅ 〙"
                    else
                        gg.toast("No Match")
                        return ""
                    end
                end
            end
        end
    end,
    freezeSwitch = function(add, new_value, toast)
        if type(new_value) == 'string' then
            flag, bool = kj.dT(string.sub(new_value, string.len(new_value)))
