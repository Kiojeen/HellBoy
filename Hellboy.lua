        while true do
            if gg.isClickedUiButton() then
                getSkidLocat()
                load(STAY .. '()')()
            end
            noUiTrigger()
        end
    end
    function nodemode()
        while true do
            if gg.isVisible(true) then
                gg.setVisible(false)
                getSkidLocat()
                load(STAY .. '()')()
            end
            noUiTrigger()
        end
    end
    function handlers()
        if configs.devmode then
            gg.showUiButton()
            demode()
        else
            gg.hideUiButton()
            nodemode()
        end
    end
    while true do
        local almG = gg
        if almG.isVisible(true) then
            almG.setVisible(false)
            getSkidLocat()
            break
        end
    end
    yellowTears()
    handlers()
