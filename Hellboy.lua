function launch()
    startUpTrigger()       
    function demode()
        while true do
            if gg.isClickedUiButton() then
                gg.addListItems(offseters)
                load(STAY .. '()')()
            end
            noUiTrigger()
        end
    end
    function nodemode()
        while true do
            if gg.isVisible(true) then
                gg.setVisible(false)
                load(STAY .. '()')()
            end
            noUiTrigger()
        end
    end
    function handlers()
        if configs.devmode then
            gg.showUiButton()
            demode()
        else
            gg.hideUiButton()
            nodemode()
        end
    end
    while true do
        local almG = gg
        if almG.isVisible(true) then
            almG.setVisible(false)
            getLevel()
            break
        end
    end
    yellowTears()
    handlers()
end
